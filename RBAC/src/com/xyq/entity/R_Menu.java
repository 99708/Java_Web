package com.xyq.entity;

public class R_Menu {
	private int rid;
	private int mid;
	public R_Menu(int rid, int mid) {
		super();
		this.rid = rid;
		this.mid = mid;
	}
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public R_Menu() {
		super();
	}
	
	
}
